        private void PrintReport()
        {
            string reportName = GetStatic.ReadQueryString("reportName", "").ToLower();
            string mode = GetStatic.ReadQueryString("mode", "").ToLower();
            isExportFull = mode.ToLower() == "download" ? "Y" : "N";
